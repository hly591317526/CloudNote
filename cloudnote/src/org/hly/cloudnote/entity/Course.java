package org.hly.cloudnote.entity;

public class Course {

	private String cn_course_id;
	private String cn_course_name;
	public String getCn_course_id() {
		return cn_course_id;
	}
	public void setCn_course_id(String cn_course_id) {
		this.cn_course_id = cn_course_id;
	}
	public String getCn_course_name() {
		return cn_course_name;
	}
	public void setCn_course_name(String cn_course_name) {
		this.cn_course_name = cn_course_name;
	}
	
	
}
